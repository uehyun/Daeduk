def add(a,b):
    return a+b

def minus(a,b):
    return a-b

def multiply(a,b):
    return a*b

def divide(a,b):
    return a/b

def namuji(a,b):
    return a%b

sum = add(4,3)
print("sum",sum)

min = minus(4,3)
print("minus",min)

mul = multiply(4,3)
print("mul",mul)

div = divide(4,3)
print("div",div)

nam = namuji(4,3)
